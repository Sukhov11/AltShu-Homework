/* Значение null представляет отсутствие какого-либо объектного значения. В JavaScript, null является примитивом, и в контексте логических операций, рассматривается как ложное. */
let food = null;
  const number = null;

  console.log(food + number); //0
  console.log(food - number); //0
  console.log(food * number); //0
  console.log(food / number); //NaN
  console.log(food ** number); //1
